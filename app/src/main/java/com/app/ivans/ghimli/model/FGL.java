package com.app.ivans.ghimli.model;

public class FGL {
    private String date;
    private String buyer;
    private String color;
    private String size;
    private String laying_planning;
    private int total;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getBuyer() {
        return buyer;
    }

    public void setBuyer(String buyer) {
        this.buyer = buyer;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getLaying_planning() {
        return laying_planning;
    }

    public void setLaying_planning(String laying_planning) {
        this.laying_planning = laying_planning;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
